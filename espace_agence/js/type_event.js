// intervenant 0
  function unhide_at0(divID) {
    var item = document.getElementById('fields_at0');
    if (item) {
      item.className=(item.className=='hidden')?'unhidden':'hidden';
    }
  }

  function hide_at0(divID){
  var item = document.getElementById('hide_at0');
  if(item){
      item.style.visibility='hidden';
      item.className=(item.className=='unhidden')?'hide':'unhidden';
    }
  }



