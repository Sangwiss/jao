// intervenant 1
  function unhide_bus(divID) {
    var item = document.getElementById('fields_bus_2');
    if (item) {
      item.className=(item.className=='hidden')?'unhidden':'hidden';
    }
  }
  
  function hide_bus(divID){
	var item = document.getElementById('hide_bus');
	if(item){
			item.style.visibility='hidden';
			item.className=(item.className=='unhidden')?'hide':'unhidden';
		} 
  }

