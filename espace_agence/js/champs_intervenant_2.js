  function unhide2(divID) {
    var item = document.getElementById('fields_22');
    if (item) {
      item.className=(item.className=='hidden')?'unhidden':'hidden';
    }
  }
  
  function hide2(divID){
	var item = document.getElementById('hide2');
	if(item){
			item.style.visibility='hidden';
			item.className=(item.className=='unhidden')?'hide':'unhidden';
		} 
  }

