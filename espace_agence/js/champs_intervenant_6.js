// intervenant 1
  function unhide6(divID) {
    var item = document.getElementById('fields_26');
    if (item) {
      item.className=(item.className=='hidden')?'unhidden':'hidden';
    }
  }
  
  function hide6(divID){
	var item = document.getElementById('hide6');
	if(item){
			item.style.visibility='hidden';
			item.className=(item.className=='unhidden')?'hide':'unhidden';
		} 
  }

