// intervenant 1
  function unhide7(divID) {
    var item = document.getElementById('fields_27');
    if (item) {
      item.className=(item.className=='hidden')?'unhidden':'hidden';
    }
  }
  
  function hide7(divID){
	var item = document.getElementById('hide7');
	if(item){
			item.style.visibility='hidden';
			item.className=(item.className=='unhidden')?'hide':'unhidden';
		} 
  }

