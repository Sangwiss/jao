// intervenant 1
  function unhide5(divID) {
    var item = document.getElementById('fields_25');
    if (item) {
      item.className=(item.className=='hidden')?'unhidden':'hidden';
    }
  }
  
  function hide5(divID){
	var item = document.getElementById('hide5');
	if(item){
			item.style.visibility='hidden';
			item.className=(item.className=='unhidden')?'hide':'unhidden';
		} 
  }

