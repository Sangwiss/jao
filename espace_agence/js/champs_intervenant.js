// intervenant 1
  function unhide(divID) {
    var item = document.getElementById('fields_2');
    if (item) {
      item.className=(item.className=='hidden')?'unhidden':'hidden';
    }
  }
  
  function hide(divID){
	var item = document.getElementById('hide');
	if(item){
			item.style.visibility='hidden';
			item.className=(item.className=='unhidden')?'hide':'unhidden';
		} 
  }

