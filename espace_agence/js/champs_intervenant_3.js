// intervenant 1
  function unhide3(divID) {
    var item = document.getElementById('fields_23');
    if (item) {
      item.className=(item.className=='hidden')?'unhidden':'hidden';
    }
  }
  
  function hide3(divID){
	var item = document.getElementById('hide3');
	if(item){
			item.style.visibility='hidden';
			item.className=(item.className=='unhidden')?'hide':'unhidden';
		} 
  }

