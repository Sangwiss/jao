// intervenant 1
  function unhide_tram(divID) {
    var item = document.getElementById('fields_tram_2');
    if (item) {
      item.className=(item.className=='hidden')?'unhidden':'hidden';
    }
  }
  
  function hide_tram(divID){
	var item = document.getElementById('hide_tram');
	if(item){
			item.style.visibility='hidden';
			item.className=(item.className=='unhidden')?'hide':'unhidden';
		} 
  }

