// intervenant 1
  function unhide4(divID) {
    var item = document.getElementById('fields_24');
    if (item) {
      item.className=(item.className=='hidden')?'unhidden':'hidden';
    }
  }
  
  function hide4(divID){
	var item = document.getElementById('hide4');
	if(item){
			item.style.visibility='hidden';
			item.className=(item.className=='unhidden')?'hide':'unhidden';
		} 
  }

